package arrays;

public class BitwiseOR {
    public static void main(String[] args) {
        int a = 3;
        int b = 5;
        int bitwiseOR = a | b;
        int bitwiseAND = a & b;
        int bitwiseXOR = a ^ b;
        System.out.println(bitwiseOR);
        System.out.println(bitwiseAND);
        System.out.println(bitwiseXOR);
    }
}
